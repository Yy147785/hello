package com.dfbz.day12;

public class Test9 {


    private int id;
    private String name;
    private double danjia;
    private String jijia;


    public Test9() {
    }

    public Test9(int id, String name, double danjia, String jijia) {
        this.id = id;
        this.name = name;
        this.danjia = danjia;
        this.jijia = jijia;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getDanjia() {
        return danjia;
    }

    public void setDanjia(double danjia) {
        this.danjia = danjia;
    }

    public String getJijia() {
        return jijia;
    }

    public void setJijia(String jijia) {
        this.jijia = jijia;
    }
}
